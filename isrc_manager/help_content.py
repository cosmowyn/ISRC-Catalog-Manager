def iter_help_screenshot_references(
    chapters: tuple[HelpChapter, ...] | list[HelpChapter] | None = None,
):
    yield from HELP_SCREENSHOT_REFERENCES
    yield from iter_help_chapter_screenshot_references(chapters)


def help_chapter_requires_screenshot(chapter_id: str) -> bool:
    return (
        str(chapter_id) not in HELP_CHAPTER_SCREENSHOT_EXEMPT_IDS
        and str(chapter_id) in HELP_CHAPTER_SCREENSHOT_BASES
    )


def iter_help_chapter_screenshot_references(
    chapters: tuple[HelpChapter, ...] | list[HelpChapter] | None = None,
):
    for chapter in chapters or HELP_CHAPTERS:
        if help_chapter_requires_screenshot(chapter.chapter_id):
            yield help_chapter_screenshot_reference(chapter)


def help_screenshot_source_dir() -> Path:
    return Path(__file__).resolve().parents[1] / "docs" / "help" / "screenshots"


def copy_help_screenshots(destination_dir: Path, source_dir: Path | None = None) -> list[Path]:
    source = Path(source_dir) if source_dir is not None else help_screenshot_source_dir()
    destination = Path(destination_dir)
    copied: list[Path] = []
    if not source.exists():
        return copied
    destination.mkdir(parents=True, exist_ok=True)
    for reference in iter_help_screenshot_references():
        source_path = source / reference.filename
        if not source_path.exists():
            continue
        destination_path = destination / reference.filename
        if (
            not destination_path.exists()
            or source_path.read_bytes() != destination_path.read_bytes()
        ):
            shutil.copy2(source_path, destination_path)
        copied.append(destination_path)
    return copied


def _screenshot_figure(reference: HelpScreenshot) -> str:
    filename = escape(reference.filename)
    title = escape(reference.title)
    alt_text = escape(reference.alt_text)
    caption = escape(reference.caption)
    return f"""
        <div class="help-screenshot">
          <p class="help-screenshot-image">
            <img src="screenshots/{filename}" alt="{alt_text}">
          </p>
          <p class="help-screenshot-caption"><strong>{title}</strong>: {caption}</p>
        </div>
        """


def render_screenshot_gallery() -> str:
    return "\n".join(_screenshot_figure(reference) for reference in HELP_SCREENSHOT_REFERENCES)


